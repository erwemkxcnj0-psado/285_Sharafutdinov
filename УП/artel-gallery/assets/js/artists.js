function escapeHtml(str) {
  return String(str)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function renderArtists() {
  const root = document.getElementById("artistsList");
  if (!root) return;

  root.innerHTML = ARTISTS.map(a => `
    <article class="artist-row card">
      <div class="artist-row__media">
        <img src="${a.photo}" alt="${escapeHtml(a.name)}" loading="lazy">
      </div>

      <div class="artist-row__body">
        <div class="artist-row__kicker">${escapeHtml(a.role)}</div>
        <div class="artist-row__name">${escapeHtml(a.name)}</div>
        <div class="artist-row__bio">${escapeHtml(a.bio)}</div>
      </div>
    </article>
  `).join("");
}

document.addEventListener("DOMContentLoaded", renderArtists);